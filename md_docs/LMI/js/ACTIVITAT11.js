// Funció que retorna el doble del número proporcionat
function doble(num) {
    return num * 2;
  }
  
  // Cridem la funció amb diferents valors i mostrem els resultats en consola
  console.log(doble(5));   // 10
  console.log(doble(10));  // 20
  console.log(doble(3));   // 6
  console.log(doble(0));   // 0
  console.log(doble(-4));  // -8